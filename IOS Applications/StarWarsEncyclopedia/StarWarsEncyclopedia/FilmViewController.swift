import UIKit

class FilmViewController: UITableViewController {
    var films = [String]()

    override func viewDidLoad() {
        super.viewDidLoad()
        StarWarsModel.getAllPeople(url: "http://swapi.co/api/films", completionHandler: { // passing what becomes "completionHandler" in the 'getAllPeople' function definition in StarWarsModel.swift
            data, response, error in
            do {
                // Try converting the JSON object to "Foundation Types" (NSDictionary, NSArray, NSString, etc.)
                if let jsonResult = try JSONSerialization.jsonObject(with: data!, options: JSONSerialization.ReadingOptions.mutableContainers) as? NSDictionary {
                    if let results = jsonResult["results"] as? NSArray {
                        for film in results {
                            if let film1 = film as? NSDictionary{
                                self.films.append(film1["title"] as! String)
                        }
                        }
                    }
                }
                DispatchQueue.main.async {
                    self.tableView.reloadData()
                }
            } catch {
                print("Something went wrong")
            }
        })
    }

